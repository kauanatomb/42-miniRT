#include "miniRT.h"

void	update_hit(float t, float *t_best, t_v3d *n_best, t_v3d *n_tmp)
{
	if (t > 0 && t < *t_best)
	{
		*t_best = t;
		*n_best = *n_tmp;
	}
}
